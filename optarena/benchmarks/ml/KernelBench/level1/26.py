def _gelu(x):
    z = x / np.sqrt(2.0)
    sign = np.where(z < 0, -1.0, 1.0)
    a = np.abs(z)
    t = 1.0 / (1.0 + 0.3275911 * a)
    erf = sign * (1.0 - (((((1.061405429 * t - 1.453152027) * t) + 1.421413741) * t - 0.284496736) * t + 0.254829592) * t * np.exp(-a * a))
    return 0.5 * x * (1.0 + erf)

class Model:
    def __init__(self):
        pass

    def forward(self, x):
        return _gelu(x)

